
'''
1. Особенный номер – строка формата [2-4 цифры]\[2-5 цифр]. Хороший номер - строка формата [4 цифры]\[5 цифр].
Хороший номер можно получить из особенного дополнением нулей слева к обоим числам.
    Пример:
    17\234 => 0017\00234 

Напишите функцию, которая принимает на вход строку и для каждого особенного номера, встречающегося в строке, выводит соответствующий хороший номер
   
'''
   
import re
    
def convert_special_to_good_number(text):
    # Используем регулярное выражение для поиска всех особенных номеров в строке
    pattern = r'(\d{2,4})\\(\d{2,5})'

    # Проходим по найденным особенным номерам и преобразуем их в хорошие номера
    def replace_special(match):
        first_part = match.group(1).rjust(4, '0')  # Дополнение первой части номера нулями слева до 4 цифр
        second_part = match.group(2).rjust(5, '0')  # Дополнение второй части номера нулями слева до 5 цифр
        return f'{first_part}\\{second_part}'

    # Заменяем особенные номеры в тексте
    result = re.sub(pattern, replace_special, text)

    return result

# Ввод текста
# text = "405\\549"
# text = "Адрес 5467\\456. Номер"
text = "17\\234"

converted_text = convert_special_to_good_number(text)

# Вывод результата
print(converted_text)
